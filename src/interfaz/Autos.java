    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
       
    int fila = tabla.getSelectedRow();
    
    
    if (fila >= 0){

        idAutoSeleccionado = Integer.parseInt(tabla.getValueAt(fila, 0).toString());

        txtMarca.setText(tabla.getValueAt(fila, 1).toString());
        txtModelo.setText(tabla.getValueAt(fila, 2).toString());
        txtColor.setText(tabla.getValueAt(fila, 3).toString());
        txtPrecio.setText(tabla.getValueAt(fila, 4).toString());

        String estado = tabla.getValueAt(fila, 5).toString();
        

        if (estado.equalsIgnoreCase("Usado")) {
            
            cbxEstado.setSelectedItem("Usado"); 
            txtDueños.setEnabled(true);
            txtKilo.setEnabled(true);
            txtDueños.setText(tabla.getValueAt(fila, 6).toString());
            txtKilo.setText(tabla.getValueAt(fila, 7).toString());
            
        } else {
            
            cbxEstado.setSelectedItem("Nuevo");
            txtDueños.setEnabled(false);
            txtKilo.setEnabled(false);
            txtDueños.setText("");
            txtKilo.setText("");
            
        }

        txtStock.setText(tabla.getValueAt(fila, 8).toString());
 
        estadoSelec();
    } 
    }//GEN-LAST:event_tablaMouseClicked


    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
    String idBuscado = txtBuscar.getText().trim();
        if (idBuscado.isEmpty()) {
        cargarTabla();
        return; 
    }

  javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel() {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; 
        }
    };
    
    modelo.addColumn("id");
    modelo.addColumn("marca");
    modelo.addColumn("modelo");
    modelo.addColumn("color");
    modelo.addColumn("precio");
    modelo.addColumn("estado");
    modelo.addColumn("dueños");
    modelo.addColumn("kilometraje");
    modelo.addColumn("stock");

    try {
        conexion cc = new conexion();
        Connection cn = cc.getConexion();
String sql = "SELECT a.*, u.numDueños, u.kilometraje " +
                     "FROM autos a " +
                     "LEFT JOIN autousado u ON a.idAuto = u.idAuto " +
                     "WHERE a.idAuto = ?";

        java.sql.PreparedStatement pst = cn.prepareStatement(sql);
        pst.setInt(1, Integer.parseInt(idBuscado));

        java.sql.ResultSet rs = pst.executeQuery();

        boolean encontro = false;

        while (rs.next()) {
            encontro = true; 
            modelo.addRow(new Object[]{
                rs.getString("idAuto"),
                rs.getString("marca"),
                rs.getString("modelo"),
                rs.getString("color"),
                rs.getString("precio"),
                rs.getString("estado"),
                rs.getString("numDueños"),
                rs.getString("kilometraje"),
                rs.getString("stock")
            });
        }
  if (!encontro) {
            javax.swing.JOptionPane.showMessageDialog(null, "No se encontró ningún auto con el ID: " + idBuscado);
            txtBuscar.setText(""); 
            cargarTabla(); 
        } else {
            tabla.setModel(modelo); 
        }

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Por favor, ingresa solo números para buscar por ID.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        txtBuscar.setText("");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error al buscar: " + e.getMessage());
    }    
    }//GEN-LAST:event_btnBuscarActionPerformed


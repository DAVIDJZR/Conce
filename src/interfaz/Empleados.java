    public void mostrarDatos() {
    String[] nombresColumnas = {"ID", "Nombre", "Puesto", "Teléfono", "Salario"};
    
    DefaultTableModel modelo = new DefaultTableModel(null, nombresColumnas) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; 
        }
    };
    
   
    String sql = "SELECT * FROM empleados";
    
    

    try{ 
        conexion cc = new conexion();
            Connection cn = cc.getConexion();
         Statement st = cn.createStatement();
         ResultSet rs = st.executeQuery(sql);

        Object[] fila = new Object[5];
        while (rs.next()) {
            fila[0] = rs.getInt("idEmpleado");
            fila[1] = rs.getString("nombre");
            fila[2] = rs.getString("puesto");
            fila[3] = rs.getString("telefono");
            fila[4] = rs.getBigDecimal("salario");
            modelo.addRow(fila);
        }
       
        jTable1.setModel(modelo);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al cargar datos: " + e.getMessage());
    }
    txtNombre.setEditable(false);
    txtPuesto.setEditable(false);
    txtTelefono.setEditable(false);
    jTextField1.setEditable(false);
    txxtSalario.setEditable(false); 
   

    btnGuardar.setEnabled(false);
    btnModificar.setEnabled(false);
    btnBorrar.setEnabled(false);
}
private boolean camposEstanLlenos() {
    if (txtNombre.getText().trim().isEmpty() || 
        txtPuesto.getText().trim().isEmpty() || 
        txtTelefono.getText().trim().isEmpty() || 
        txxtSalario.getText().trim().isEmpty()) {
        
        return false; 
    }
    return true; 
}    
    

